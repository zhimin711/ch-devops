DROP TABLE IF EXISTS `bt_cluster`;
CREATE TABLE `bt_cluster` (
  `ID` BIGINT(20) NOT NULL AUTO_INCREMENT COMMENT '主键',
  `CLUSTER_NAME` VARCHAR(50) NULL COMMENT '集群名称',
  `ZOOKEEPER` VARCHAR(250) NULL COMMENT 'ZOOKEEPER',
  `BROKERS` VARCHAR(250) NULL COMMENT 'BROKERS',
  `DESCRIPTION` VARCHAR(255) DEFAULT NULL COMMENT '描述',
  `STATUS` CHAR(1) NOT NULL DEFAULT '0' COMMENT '状态：0. 1. 2.',
  `CREATE_AT` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `CREATE_BY` VARCHAR(64) NULL COMMENT '创建人',
  `UPDATE_AT` DATETIME NULL COMMENT '更新时间',
  `UPDATE_BY` VARCHAR(64) NULL COMMENT '更新人',
  PRIMARY KEY (`ID`),
  UNIQUE KEY `UK_BT_TE_CN_TN`(`CLUSTER_NAME`),
  KEY `IDX_BT_TE_STATUS`(`STATUS`)
) ENGINE=INNODB DEFAULT CHARSET=UTF8 COMMENT '业务表-集群配置信息';