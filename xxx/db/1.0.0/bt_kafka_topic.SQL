DROP TABLE IF EXISTS `bt_kafka_topic`;
CREATE TABLE `bt_kafka_topic`
(
  `ID`             BIGINT(20)   NOT NULL AUTO_INCREMENT COMMENT '主键',
  `CLUSTER_NAME`   VARCHAR(50)  NULL COMMENT '集群名称',
  `TOPIC_NAME`     VARCHAR(150)  NULL COMMENT '主题名称',
  `TYPE`           VARCHAR(20)  NOT NULL COMMENT '存储类型：STRING, JSON, PROTO_STUFF',
  `CLASS_FILE`     VARCHAR(50)  NULL COMMENT '类文件',
  `CLASS_NAME`     VARCHAR(150) NULL COMMENT '类名称',
  `PARTITION_SIZE` int(2)       NULL COMMENT '分区数',
  `REPLICA_SIZE`   int(2)       NULL COMMENT '复制数（备份）',
  `DESCRIPTION`    VARCHAR(255)          DEFAULT NULL COMMENT '描述',
  `STATUS`         CHAR(1)      NOT NULL DEFAULT '0' COMMENT '状态：0.禁用 1.启用 2. 3.删除',
  `CREATE_AT`      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `CREATE_BY`      VARCHAR(64)  NULL COMMENT '创建人',
  `UPDATE_AT`      DATETIME     NULL COMMENT '更新时间',
  `UPDATE_BY`      VARCHAR(64)  NULL COMMENT '更新人',
  PRIMARY KEY (`ID`),
  UNIQUE KEY `UK_BT_TE_CN_TN` (`CLUSTER_NAME`, `TOPIC_NAME`),
  KEY `IDX_BT_TE_STATUS` (`STATUS`)
) ENGINE = INNODB
  DEFAULT CHARSET = UTF8 COMMENT '业务表-主题信息';