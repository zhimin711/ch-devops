DROP TABLE IF EXISTS `bt_content_record`;
CREATE TABLE `bt_content_record` (
  `ID` BIGINT(20) NOT NULL AUTO_INCREMENT COMMENT '主键',
  `SID` BIGINT(20) NOT NULL COMMENT '搜索记录主键',
  `PARTITION_ID` INT(2) NOT NULL COMMENT '分区ID',
  `MESSAGE_OFFSET` BIGINT(20) NOT NULL COMMENT '分区索引',
  `CONTENT` text DEFAULT NULL COMMENT '描述',
  PRIMARY KEY (`ID`),
  KEY `IDX_BT_CR_SID`(`SID`)
) ENGINE=INNODB DEFAULT CHARSET=UTF8 COMMENT '搜索记录表';